#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t j;
	size_t res;

	i = 0;
	while (dst[i] && i <= dstsize)
		i++;
	if (dstsize < i)
		return (dstsize + ft_strlen(src));
	res = i + ft_strlen(src);
	j = 0;
	while (src[j] && i + 1 < dstsize)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (res);
}
